// ---- No-op C++ exception-frame registration -------------------------------
// Linked via -Wl,--wrap. frame_dummy calls __register_frame_info at static init
// to register the .eh_frame table for exception unwinding; on SH4/KOS the
// libgcc-internal object_mutex lock no-ops at that point and its unlock asserts,
// aborting boot. We build -fno-exceptions so unwinding is never used — skip the
// registration entirely. (If C++ exceptions are ever needed on DC, fix the KOS
// early-boot mutex instead of no-opping these.)
extern "C" void __wrap___register_frame_info(const void* /*begin*/, void* /*ob*/) {}
extern "C" void* __wrap___deregister_frame_info(const void* /*begin*/) { return 0; }

// Safety net: log any genuine bad mutex unlock (count<=0 = not locked) to serial
// with its caller, so a future imbalance is diagnosable. IRQ-context unlocks
// (holder==IRQ_THREAD, count==1) are valid and skipped by the count check.
extern "C" int __real_mutex_unlock(mutex_t* m);
extern "C" int __wrap_mutex_unlock(mutex_t* m)
{
    if (m != NULL && m->count <= 0)
    {
        printf("[BAD-UNLOCK] ret=%p m=%p holder=%p cur=%p count=%d\n",
               __builtin_return_address(0), (void*)m, (void*)m->holder,
               (void*)thd_current, m->count);
    }
    return __real_mutex_unlock(m);
}

// ---- Single-threaded static-init guards -----------------------------------
// libstdc++'s __cxa_guard_acquire (function-local `static` init guard) locks a
// recursive __gnu_cxx::__mutex via KOS mutex_lock, which fails during C++ static
// init (see the mutex fix below) -> __throw_concurrence_lock_error -> (unwinding
// is stripped by the frame-info no-ops) -> infinite exception re-throw, which was
// the original boot hang. On the Dreamcast all global/static constructors run
// single-threaded on the kernel thread before main(), so thread-safe guards are
// unnecessary. Replace them with trivial non-atomic versions (identical to what
// -fno-threadsafe-statics emits inline): no mutex, no throw. The Itanium ABI
// guard's first byte is the "initialized" flag.
extern "C" int __wrap___cxa_guard_acquire(unsigned long long* g)
{
    return *reinterpret_cast<volatile char*>(g) == 0;
}
extern "C" void __wrap___cxa_guard_release(unsigned long long* g)
{
    *reinterpret_cast<volatile char*>(g) = 1;
}
extern "C" void __wrap___cxa_guard_abort(unsigned long long* /*g*/) {}

// SYSTEMIC KOS/libstdc++ mutex fix. KOS mutex_lock() spuriously rejects
// libstdc++'s statically-initialized mutexes (both normal and recursive) during
// C++ static init — seen from __cxa_guard_acquire, __exchange_and_add (refcount),
// etc. — returning nonzero and driving __throw_concurrence_lock_error into an
// infinite re-throw (unwinding is stripped). All boot-time locking is
// single-threaded on the kernel thread, so when the real lock fails on an
// UNCONTENDED mutex (holder==NULL) we force-acquire it: set holder=current,
// count=1. The paired mutex_unlock (same thread) then satisfies KOS's
// owner+count assertion and releases cleanly. Genuinely contended failures
// (holder!=NULL) are left to fail. A counter logs the first few for visibility.
extern "C" int __real_mutex_lock(mutex_t*);
extern "C" int __wrap_mutex_lock(mutex_t* m)
{
    int rv = __real_mutex_lock(m);
    if (rv != 0 && m != NULL && m->holder == NULL)
    {
        // Uncontended mutex KOS spuriously rejected — force-acquire it.
        m->holder = thd_current;
        m->count  = 1;
        return 0;
    }
    return rv;
}

// ---- SH4 exception catcher (debug aid) ------------------------------------
// A global SH4 exception handler that, on an otherwise-fatal fault, dumps the
// faulting PC to serial and paints the screen red, then spins. It is
// EXCEPTION-CONTEXT SAFE: it must NOT call bfont (bfont_draw_* internally calls
// thd_poll, which asserts !irq_inside_int() and secondary-crashes) or stdio
// printf (locks). snprintf to a stack buffer (no locks) + dbgio_write_str
// (busy-polled SCIF, no thd_poll) + a direct-VRAM fill are all safe. It draws
// nothing at boot, only on an actual fault — invaluable on real hardware.
static volatile int sInExc = 0;

static void DcExcHandler(irq_t code, irq_context_t* ctx, void* data)
{
    (void)data;
    // KOS calls the global handler FIRST on EVERY interrupt/exception, then the
    // specific handler. So this fires on every scheduler timer tick (TMU0=0x400)
    // and every external IRQ — we MUST return for those so KOS's timer/scheduler
    // keeps running. Act only on genuine CPU faults (EXPEVT): TLB miss/protection,
    // address error (unaligned/bad pointer), illegal instruction, FPU.
    switch ((unsigned)code)
    {
        case 0x040: case 0x060:   // TLB miss (read/write)
        case 0x0a0: case 0x0c0:   // TLB protection violation
        case 0x0e0: case 0x100:   // address error (read/write)
        case 0x180: case 0x1a0:   // illegal / slot-illegal instruction
        case 0x800: case 0x820:   // FPU exception
            break;                // real fault -> report + freeze
        default:
            return;               // interrupt / TRAPA / etc. -> let KOS handle
    }
    if (sInExc) { for (;;) {} }
    sInExc = 1;
    char msg[160];
    snprintf(msg, sizeof(msg),
             "\n*** SH4 EXCEPTION *** PC=%08lx CODE=%03x -- photograph this\n",
             (unsigned long)CONTEXT_PC(*ctx), (unsigned)code);
    dbgio_write_str(msg);
    vid_set_mode(DM_640x480, PM_RGB565);
    for (int i = 0; i < 640 * 480; ++i) vram_s[i] = 0xF800; // red
    for (;;) {}
}

__attribute__((constructor(101)))
static void DcArmExcHandler(void)
{
    arch_irq_set_global_handler(&DcExcHandler, NULL);
}

static int    sArgc = 0;
static char** sArgv = nullptr;

static void* EngineThread(void* /*param*/)
{
    GameMain(sArgc, sArgv);
    return nullptr;
}

int main(int argc, char** argv)
{
    (void)gDcLogSink;

    // Route stdout (and thus the engine's SYS_Log/printf) to the SCIF serial
    // port so flycast's Serial Console shows the boot log live. The last line
    // before the crash pinpoints the faulting subsystem. setbuf(NULL) so nothing
    // is lost when the SH4 faults.
    dbgio_dev_select("scif");
    setbuf(stdout, NULL);
    printf("=== Polyphase Dreamcast boot (serial) ===\n");

    sArgc = argc;
    sArgv = argv;

    // KOS's default main-thread stack is only 32 KB (THD_STACK_SIZE) — too small
    // for the engine's init (deep call chains, large scene/reflection frames).
    // Run GameMain on a dedicated 2 MB-stack thread.
    kthread_attr_t attr = {};
    attr.stack_size = 2 * 1024 * 1024;
    attr.prio       = PRIO_DEFAULT;
    attr.label      = "polyphase";
    kthread_t* engine = thd_create_ex(&attr, EngineThread, NULL);
    if (engine != NULL)
        thd_join(engine, NULL);
    else
        GameMain(argc, argv);

    return 0;
}

