void OctPostInitialize()
{
    // ReadEngineConfig (inside GameMain) reloads Config.ini AFTER
    // OctPreInitialize and overwrites the 640x480 with the project's authored
    // desktop size; Initialize then copies that into EngineState. Re-force it
    // here so Renderer::GetViewportWidth/Height (read live every frame) sees
    // the native DC resolution. PostPackage also rewrites the packaged
    // Config.ini to 640x480, but this defends manual/older packages too.
    const uint32_t beforeW = GetEngineState()->mWindowWidth;
    const uint32_t beforeH = GetEngineState()->mWindowHeight;
    GetEngineState()->mWindowWidth  = 640;
    GetEngineState()->mWindowHeight = 480;
    LogDebug("[DC] OctPostInitialize: window %ux%u -> 640x480", beforeW, beforeH);

    DcSpawnDemoScene();
}

